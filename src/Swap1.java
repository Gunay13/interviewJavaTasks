public class Swap1 {
    public static void main(String[] args) {
        int a=5;
        int b=10;
        System.out.println("a = " + a);
        System.out.println("b = " + b);
        System.out.println("*******************");
        int temp=0;
        temp=a;
        a=b;
        b=temp;
        System.out.println("a = " + a);
        System.out.println("b = " + b);


    }
}
